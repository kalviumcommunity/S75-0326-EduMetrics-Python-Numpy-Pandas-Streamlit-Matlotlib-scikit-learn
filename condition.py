# Program 2: if-else
print("Program 2: if-else")
num = 7

if num % 2 == 0:
    print("Even")
else:
    print("Odd")

print()

# Program 3: if-elif-else
print("Program 3: if-elif-else")
marks = 85

if marks >= 90:
    print("Grade A")
elif marks >= 70:
    print("Grade B")
else:
    print("Grade C")

print()

